module flower {
	export class Formula {
		public static add(a:number,b:number):number
		{
			return a + b;
		}

		public static minus(a:number,b:number):number
		{
			return a - b;
		}

		public static multiply(a:number,b:number):number
		{
			return a * b;
		}

		public static device(a:number,b:number):number
		{
			return a / b;
		}

	}
}

